(* Generate TypeScript type definitions *)

(* ── Type mapping ────────────────────────────────────────────────── *)

let rec ts_type = function
  | Ast.Prim Ast.String -> "string"
  | Ast.Prim Ast.Int -> "number"
  | Ast.Prim Ast.Int64 -> "bigint"
  | Ast.Prim Ast.Float -> "number"
  | Ast.Prim Ast.Byte -> "number"
  | Ast.Prim Ast.Bool -> "boolean"
  | Ast.Prim Ast.Binary -> "Uint8Array"
  | Ast.Prim Ast.Uuid -> "string"
  | Ast.Prim Ast.Uuid_v7 -> "string"
  | Ast.Prim Ast.Void -> "void"
  | Ast.Named n -> n
  | Ast.Ref n -> Emitter.ref_id n
  | Ast.Option t -> ts_type t ^ " | undefined"
  | Ast.Tuple ts -> "[" ^ String.concat ", " (List.map ts_type ts) ^ "]"
  | Ast.List t -> ts_type t ^ "[]"
  | Ast.Set t -> "Set<" ^ ts_type t ^ ">"
  | Ast.Map (k, v) -> "Record<" ^ ts_type k ^ ", " ^ ts_type v ^ ">"

(* ── Literal → TypeScript ────────────────────────────────────────── *)

let rec lit_to_ts = function
  | Ast.Lit_string s -> "\"" ^ s ^ "\""
  | Ast.Lit_int i -> string_of_int i
  | Ast.Lit_float f -> string_of_float f
  | Ast.Lit_bool b -> string_of_bool b
  | Ast.Lit_array xs -> "[" ^ String.concat ", " (List.map lit_to_ts xs) ^ "]"

(* ── Section emitters ────────────────────────────────────────────── *)

let emit_branded_ids line blank refs =
  List.iter (fun name ->
    let id = Emitter.ref_id name in
    line (Printf.sprintf "export type %s = string & { readonly %s: \"%s\" }" id Emitter.codegen_brand id)
  ) refs;
  if refs <> [] then blank ()

let emit_const line blank = function
  | Ast.Const c ->
    line (Printf.sprintf "export const %s = %s as const" c.name (lit_to_ts c.value));
    blank ()
  | _ -> ()

let emit_struct line blank = function
  | Ast.Struct s ->
    line (Printf.sprintf "export interface %s {" s.name);
    List.iter (fun (f : Ast.field) ->
      let opt = if f.optional then "?" else "" in
      line (Printf.sprintf "  %s%s: %s" f.name opt (ts_type f.typ))
    ) s.fields;
    line "}";
    blank ()
  | _ -> ()

let emit_enum line blank = function
  | Ast.Enum e ->
    line (Printf.sprintf "export type %s =" e.name);
    List.iter (fun (m : Ast.enum_member) ->
      line (Printf.sprintf "  | \"%s\"" m.name)
    ) e.members;
    blank ()
  | _ -> ()

let emit_union line blank = function
  | Ast.Union u ->
    let variants = List.map ts_type u.variants in
    line (Printf.sprintf "export type %s = %s" u.name (String.concat " | " variants));
    blank ()
  | _ -> ()

let emit_service line blank = function
  | Ast.Service s ->
    line (Printf.sprintf "export interface %sHandlers {" s.name);
    List.iter (fun (m : Ast.method_decl) ->
      let params = List.map (fun (p : Ast.param) ->
        p.name ^ ": " ^ ts_type p.typ
      ) m.params in
      let ret = ts_type m.return_type in
      let raises_comment = match m.raises with
        | [] -> ""
        | rs -> " // throws " ^ String.concat " | " rs
      in
      line (Printf.sprintf "  %s(%s): Promise<%s>%s"
        m.name (String.concat ", " params) ret raises_comment)
    ) s.methods;
    line "}";
    blank ()
  | _ -> ()

(* ── Main entry point ────────────────────────────────────────────── *)

let generate (file : Ast.file) : string =
  let buf = Buffer.create 4096 in
  let line s = Buffer.add_string buf s; Buffer.add_char buf '\n' in
  let blank () = Buffer.add_char buf '\n' in

  line "// Auto-generated by Vessel — do not edit";
  blank ();

  let refs = Emitter.collect_ref_types file in
  emit_branded_ids line blank refs;

  let decls = file.declarations in
  List.iter (emit_const line blank) decls;
  List.iter (emit_struct line blank) decls;
  List.iter (emit_enum line blank) decls;
  List.iter (emit_union line blank) decls;
  List.iter (emit_service line blank) decls;

  Buffer.contents buf
