(* Generate Rust type definitions — straightforward AST-to-Rust reflection.
   No macros, no derive magic, no lifetime annotations. Just structs,
   enums, traits, type aliases, and consts. *)

(* ── Type mapping ────────────────────────────────────────────────── *)

let rec rust_type = function
  | Ast.Prim Ast.String  -> "String"
  | Ast.Prim Ast.Int     -> "i32"
  | Ast.Prim Ast.Int64   -> "i64"
  | Ast.Prim Ast.Float   -> "f64"
  | Ast.Prim Ast.Byte    -> "u8"
  | Ast.Prim Ast.Bool    -> "bool"
  | Ast.Prim Ast.Binary  -> "Vec<u8>"
  | Ast.Prim Ast.Uuid    -> "String"
  | Ast.Prim Ast.Uuid_v7 -> "String"
  | Ast.Prim Ast.Void    -> "()"
  | Ast.Named n          -> n
  | Ast.Ref n            -> Emitter.ref_id n
  | Ast.Option t         -> "Option<" ^ rust_type t ^ ">"
  | Ast.Tuple ts         -> "(" ^ String.concat ", " (List.map rust_type ts) ^ ")"
  | Ast.List t           -> "Vec<" ^ rust_type t ^ ">"
  | Ast.Set t            -> "HashSet<" ^ rust_type t ^ ">"
  | Ast.Map (k, v)       -> "HashMap<" ^ rust_type k ^ ", " ^ rust_type v ^ ">"

(* ── Name conventions ────────────────────────────────────────────── *)

let snake_case name =
  let buf = Buffer.create (String.length name + 4) in
  String.iteri (fun i c ->
    match c with
    | 'A'..'Z' ->
      if i > 0 then Buffer.add_char buf '_';
      Buffer.add_char buf (Char.lowercase_ascii c)
    | _ ->
      Buffer.add_char buf c
  ) name;
  Buffer.contents buf

(* ── Literal → Rust ──────────────────────────────────────────────── *)

let rec lit_to_rust = function
  | Ast.Lit_string s  -> "\"" ^ s ^ "\""
  | Ast.Lit_int i     -> string_of_int i
  | Ast.Lit_float f   -> string_of_float f
  | Ast.Lit_bool b    -> string_of_bool b
  | Ast.Lit_array xs  -> "vec![" ^ String.concat ", " (List.map lit_to_rust xs) ^ "]"

(* ── Section emitters ────────────────────────────────────────────── *)

let emit_ref_type_aliases line blank refs =
  List.iter (fun name ->
    let id = Emitter.ref_id name in
    line (Printf.sprintf "pub type %s = String;" id)
  ) refs;
  if refs <> [] then blank ()

let emit_const line blank = function
  | Ast.Const c ->
    let rust_val = lit_to_rust c.value in
    line (Printf.sprintf "pub const %s: &str = %s;"
      (String.uppercase_ascii (snake_case c.name)) rust_val);
    blank ()
  | _ -> ()

let emit_struct line blank = function
  | Ast.Struct s ->
    line (Printf.sprintf "pub struct %s {" s.name);
    List.iter (fun (f : Ast.field) ->
      let typ = rust_type f.typ in
      let typ = if f.optional then "Option<" ^ typ ^ ">" else typ in
      line (Printf.sprintf "    pub %s: %s," (snake_case f.name) typ)
    ) s.fields;
    line "}";
    blank ()
  | _ -> ()

let emit_enum line blank = function
  | Ast.Enum e ->
    line (Printf.sprintf "pub enum %s {" e.name);
    List.iter (fun (m : Ast.enum_member) ->
      line (Printf.sprintf "    %s," m.name)
    ) e.members;
    line "}";
    blank ()
  | _ -> ()

let emit_union line blank = function
  | Ast.Union u ->
    line (Printf.sprintf "pub enum %s {" u.name);
    List.iteri (fun i t ->
      let variant_name = Printf.sprintf "Variant%d" (i + 1) in
      line (Printf.sprintf "    %s(%s)," variant_name (rust_type t))
    ) u.variants;
    line "}";
    blank ()
  | _ -> ()

let emit_service line blank = function
  | Ast.Service s ->
    let trait_name = s.name ^ "Handlers" in
    line (Printf.sprintf "pub trait %s {" trait_name);
    List.iter (fun (m : Ast.method_decl) ->
      let params = List.map (fun (p : Ast.param) ->
        snake_case p.name ^ ": " ^ rust_type p.typ
      ) m.params in
      let params_str = match params with
        | [] -> "&self"
        | ps -> "&self, " ^ String.concat ", " ps
      in
      let ret = match m.return_type with
        | Ast.Prim Ast.Void -> "Result<()>"
        | t -> "Result<" ^ rust_type t ^ ">"
      in
      line (Printf.sprintf "    fn %s(%s) -> %s;" (snake_case m.name) params_str ret)
    ) s.methods;
    line "}";
    blank ()
  | _ -> ()

(* ── Main entry point ────────────────────────────────────────────── *)

let generate (file : Ast.file) : string =
  let buf = Buffer.create 4096 in
  let line s = Buffer.add_string buf s; Buffer.add_char buf '\n' in
  let blank () = Buffer.add_char buf '\n' in

  line "// Auto-generated by Vessel — do not edit";
  blank ();

  let has_set = ref false in
  let has_map = ref false in
  let rec scan = function
    | Ast.Set t -> has_set := true; scan t
    | Ast.Map (k, v) -> has_map := true; scan k; scan v
    | Ast.Option t | Ast.List t -> scan t
    | Ast.Tuple ts -> List.iter scan ts
    | _ -> ()
  in
  List.iter (function
    | Ast.Struct s -> List.iter (fun (f : Ast.field) -> scan f.typ) s.fields
    | Ast.Service s -> List.iter (fun (m : Ast.method_decl) ->
        List.iter (fun (p : Ast.param) -> scan p.typ) m.params;
        scan m.return_type
      ) s.methods
    | Ast.Union u -> List.iter scan u.variants
    | _ -> ()
  ) file.declarations;

  if !has_set then line "use std::collections::HashSet;";
  if !has_map then line "use std::collections::HashMap;";
  if !has_set || !has_map then blank ();

  let refs = Emitter.collect_ref_types file in
  emit_ref_type_aliases line blank refs;

  let decls = file.declarations in
  List.iter (emit_const line blank) decls;
  List.iter (emit_struct line blank) decls;
  List.iter (emit_enum line blank) decls;
  List.iter (emit_union line blank) decls;
  List.iter (emit_service line blank) decls;

  Buffer.contents buf
